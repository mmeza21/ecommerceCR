<h2>Condiciones del Producto</h2>
<p class="text-justify">
Para hacer uso de la política de cambio y/o devoluciones, se debe considerar lo siguiente:</p>
<ul class="lista2">
<li>No se realizarán cambios o devoluciones si el mueble ha sido personalizado a solicitud del cliente.</li>
<li>El producto debe estar nuevo y sin señales de uso o daño.</li>
<li>El producto debe incluir todos los accesorios, bonificaciones y/o regalos promocionales que se obtuvieron al momento de la compra.</li>
<li>Se realizarán cambios de diseño sin consentimiento del cliente si el mueble requiere ajustes para mejor estabilidad y funcionamiento.</li>
No se aceptan devoluciones o cambios de productos y/o accesorios o regalos usados, ni con falla o deterioro atribuible a un mal uso por parte del cliente.</li>
<li>El producto puede estar abierto, pero debe estar con sus embalajes originales completos y en perfectas condiciones. No pueden estar rotas las etiquetas o gráficas del producto u otros similares que hayan acompañado al producto en la entrega al cliente.</li>
<li>Si el producto fue vendido con una promoción, ésta también debe ser devuelta en conjunto con el producto principal.</li>
<li>En el caso que no se cumpla una o más de las condiciones expresadas en el punto anterior, el cliente podrá hacer uso de la política de cambios y devoluciones, cancelando un 10% del costo pagado por el producto. Este costo considera el desarmado del producto, re-embalajes y costos varios de recupero del producto. En el caso que no se cumpla con la condición: “3”, el cliente podrá hacer uso de la política de cambios y/o devoluciones cancelando un 30% del costo cancelado por el producto. Este costo considera el desarmado del producto, re-embalajes y costos varios de recupero del producto.</li></ul>
