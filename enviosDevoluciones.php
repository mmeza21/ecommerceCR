<p class="text-justify">
En caso que el producto adquirido: No cuente con las características técnicas informadas o si estuviera dañado, incompleto al momento de la entrega, se procederá al cambio. Para lo cual se coordinará con el cliente, su realización a la brevedad. Si presentara fallas o defectos dentro de los 15 días siguientes a la fecha en que éste fue recibido, puede optarse entre: Su reparación gratuita, reparación con costo, o la devolución de la cantidad pagada; siempre que el producto no se hubiera deteriorado por un hecho imputable al consumidor. Todos los productos de “La Empresa” cuentan con garantía, por lo que el usted deberá respetar los términos allí establecidos. Para obtener estos beneficios de “La Empresa”, el cliente deberá:</p>
