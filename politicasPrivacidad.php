<h1>Política de Privacidad</h1> <hr>
<p class="text-justify">
El cliente autoriza a Comercial Rosmery, con RUC No. 1602104614, ubicado para estos efectos en provincia y departamento de Lima a realizar el tratamiento de los datos que se declaren a través del presente canal (en adelante, los “Datos Personales”).</p>
<p class="text-justify">
Los Datos Personales serán objeto de tratamiento por parte de Comercial Rosmery y éste declara que cualquier tratamiento de los Datos Personales se ajusta en las normativas de protección de datos personales vigentes y aplicables dentro del territorio peruano.</p>

<h2>Objetivo</h2>
<p class="text-justify">
La presente Política tiene por objeto hacer de conocimiento del público nuestro compromiso con la protección de datos personales, así como los lineamientos bajo los cuales realizamos el tratamiento de los mismos en ejercicio de nuestras actividades comerciales, la finalidad para la que lo hacemos, así como los procedimientos para que los titulares de los mismos puedan ejercer los derechos de acceso, rectificación, cancelación y oposición previstos en la Normativa de Protección de Datos Personales.</p>

<h2>Información general</h2>
<p class="text-justify">
Comercial Rosmery, en adelante “La Empresa”, se dedica a la venta online de muebles de hogar, comedores, sillas, mesas, muebles de dormitorio, etc.</p>
<p class="text-justify">
Como parte de su actividad, usa y trata datos personales en cumplimiento con lo dispuesto en la Ley 29733 – Ley de Protección de Datos Personales, su Reglamento, aprobado por Decreto Supremo 003-2013-JUS, y las normas complementarias y modificatorias de las anteriores (todas éstas en adelante definidas como la “Normativa de Protección de Datos Personales”). Los datos personales cuyo tratamiento realiza “La Empresa” son almacenados en bancos de datos personales de titularidad de “La Empresa”.</p>
<p class="text-justify">
Información Sobre el Tratamiento Obligatorio de Datos Personales
“La Empresa” informa al cliente que, para la prestación de los servicios y/o ejecución de las disposiciones establecidas en los Términos y Condiciones aceptados, realizará el tratamiento de su nombre, apellidos, No. de documento de identidad, dirección, correo electrónico, contraseña y/o teléfono mientras dure la relación contractual. Este tratamiento será realizado por Comercial Rosmery de forma directa y/o a través de sus proveedores , así como otras empresas encargadas del procesamiento y almacenamiento de datos.</p>
<p class="text-justify">
“La Empresa” cumple con informarle al cliente que la información mencionada en el párrafo precedente es necesaria para el cumplimiento de las obligaciones a su cargo, las mismas que de otra forma no podrían ejecutarse. Entre los servicios y/o disposiciones mencionados se encuentran: i) la creación y gestión de cuenta de usuario, ii) la coordinación, entrega de productos y postventa (medición de satisfacción, registro de vales/cupones), y, iii) la atención de solicitudes y reclamos.</p>

<h2>Finalidades Adicionales de Tratamiento:</h2>  
<p class="text-justify">
Con la aceptación de la presente Política de Privacidad el cliente otorga su consentimiento para que los Datos Personales sean tratados por un plazo indefinido, y mientras no solicite su cancelación o revoque la presente autorización, para las siguientes finalidades adicionales:</p>
<ol type="a" class="lista">
<li>Enviar al cliente información comercial, promocional y/o publicidad con relación a los productos y/o servicios brindados por «La Empresa», así como realizar prospección comercial. El contacto podrá realizarse a través de los siguientes medios: centros de llamada (call centers), sistemas de llamado telefónico, envío de mensajes de texto a celular o de mensajes electrónicos masivos y telemercadeo.</li>
<li>Almacenar y tratar los Datos Personales con fines estadísticos y/o históricos.</li></ol>
<h2>Transferencia de Datos Personales:</h2>
<ul class="lista2">
<li>Realizar los mismos usos descritos en los literales 1 y 2 anteriores;</li>
<li>Evaluar su calidad crediticia, capacidad de pago, y de ser el caso, efectuar las gestiones de recuperación o cobranza; y,</li>
<li>Evaluar solicitudes que efectúe el cliente en el presente y/o futuro.</li></ul>
<p class="text-justify">
Cabe precisar que la realización de las finalidades adicionales señaladas en este apartado requiere un consentimiento expreso y libre por parte del cliente, y que su no aceptación no impedirá la prestación de los servicios y/o ejecución de las disposiciones establecidas en los Términos y Condiciones aceptados</p>

<h2 class="text-justify">Autorización para la Obtención y Actualización de Datos Personales y Consulta de Fuentes Públicas y/o Autorizadas</h2>
<p class="text-justify">
El titular de los datos personales autoriza a “La Empresa” a tratar sus datos personales obtenidos de fuentes públicas, gratuitas o no, o de fuentes no públicas, siempre que dichas fuentes cuenten con su autorización para tratar y transferir dichos datos personales. Los datos personales recabados podrán ser tratados y transferidos cumpliendo los fines enumerados en la presente Política, así como al amparo de los principios de legalidad, proporcionalidad y finalidad recogidos en la normativa de protección de datos personales</p>

<h2>Derechos de los titulares</h2>
<p class="text-justify">
De acuerdo con la Ley, los titulares de datos personales tienen los siguientes derechos: derecho de acceso e información, derecho de rectificación, actualización e inclusión, derecho de cancelación o supresión y derecho de oposición.</p>
<ol type="1" class="lista">
<li>Derecho de Acceso e información: El titular de datos personales tiene derecho a obtener la información que sobre sí mismo sea objeto de tratamiento en bancos de datos de titularidad de “La Empresa”.</li>
<li>Derecho de rectificación, actualización e inclusión:El titular de datos personales tiene derecho a la actualización, inclusión y rectificación de sus datos personales materia de tratamiento por parte de “La Empresa” cuando estos sean parcial o totalmente inexactos, incompletos o cuando se hubiere advertido omisión, error o falsedad.</li>
<li>Derecho de Cancelación o Supresión: El titular de datos personales podrá solicitar la cancelación o supresión de sus datos personales no relacionados o necesarios para la ejecución de las obligaciones de cargo de “La Empresa” previstas en los contratos suscritos o las dispuestas por la normativa vigente.</li>
<li>Derecho de Oposición: El titular de datos personales puede oponerse al tratamiento de sus datos personales en cualquier momento. La oposición procederá en la medida que el tratamiento no tenga justificación contractual o legal.</li></ol>
<h2>Derechos de los titulares</h2>
<p class="text-justify">
Con la aceptación de la presente Política usted manifiesta su consentimiento y conformidad con todos los términos en esta expuestos y nos autoriza a tratar sus datos personales para los fines aquí expresados.</p>

<h2>Vigencia</h2>
<p class="text-justify">
Los Datos Personales que sean almacenados, utilizados o transmitidos permanecerán en las Bases de Datos de “La Empresa” durante el tiempo que sea necesario para cumplir los fines previstos en la presente Política. En el caso de clientes y ex clientes, trabajadores, contratistas y proveedores, sus datos personales relacionados al cumplimiento o ejecución de su relación comercial serán mantenidos cuando menos por el plazo que disponga la normativa aplicable.</p>

