Para todo cambio por satisfacción, se deberá pagar un valor por el recojo del producto. Este monto será igual al valor del despacho, cancelado al momento de la compra. Dicho pago deberá ser realizado por transferencia bancaria a la cuenta corriente de “La Empresa”. Esta información será brindada cuando el cliente se ponga en contacto con el “SAT” al Número: +51 933 076 787 o al Correo: servicioalcliente@comercialrosmery.com, antes del recojo del producto.</p>

